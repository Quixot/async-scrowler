function callback_one($response, $info, $request) {
  global $regexpPagin1;
  global $regexpPagin2;
  global $qOfPaginationPages;
  global $bad_urls;
	global $regexpPrices1;
	global $regexpPrices2;
	global $regexpPrices3;
	global $itemsArray;
	global $errorsArray;
	global $regexpRegion;
	global $regionRu;
	global $guide;

	AngryCurl::add_debug_msg(PHP_EOL.$request->url);
	AngryCurl::add_debug_msg($info['http_code']);
	AngryCurl::add_debug_msg($request->options[10004]);

  if ($info['http_code'] !== 200) {            
    $bad_urls[$request->url] = array(
    		$info['http_code'],
    		$request->options[10004]
    	);
  } else {
  	file_put_contents(AC_DIR.'/engines/'.ENGINE_CURR.'/content.txt', $response);
		preg_match($regexpPagin1, $response, $matches);
		@preg_match_all($regexpPagin2, $matches[1], $matches2);
		print_r($matches[1]);
		$temparrpage = array();
		foreach ($matches2[1] as $key => $value) {
			echo "value:" . $value . "\n";
			if (is_numeric($value)) {
				$temparrpage[] = $value;
			}
		}
		if (@max($temparrpage) > $qOfPaginationPages) {
			$qOfPaginationPages = @max($temparrpage);	    	
		}

  	preg_match($regexpRegion, $response, $mregion);
  	$mregion[1] = iconv('windows-1251','utf-8', trim($mregion[1]));
  	AngryCurl::add_debug_msg($mregion[1].' | '.$regionRu);

  	if ($mregion[1] == $regionRu) {
	  	preg_match_all($regexpPrices1, $response, $matches2, PREG_SET_ORDER);
			foreach ($matches2 as $key) {
				if (stripos($key[1], 'itemPrice') !== false) {
					preg_match($regexpPrices2, $key[1], $matches);
					$matches[1] = 'http://'.ENGINE_CURR.trim($matches[1]);
					if ($matches[4]) {
						$guide[$matches[4]] = trim($matches[1]);
					}
					$matches = clean_info($matches, array(1,2,3), 'windows-1251');
					price_change_detect($matches[1], $matches[2], $matches[3], date("d.m.y-H:i:s"), $request->options[10004], $request->options[10006], $request->options[10018], ENGINE_CURR, ENGINE_TYPE);
					$itemsArray[$matches[1]] = array(
						$matches[2], 
						$matches[3],
						date("d.m.y-H:i:s"),
						$request->options[10004],
						$request->options[10018],
						$request->url
					);			
					AngryCurl::add_debug_msg($matches[1].' | '.$matches[2].' | '.$matches[3]);
				} else {
					preg_match($regexpPrices3, $key[1], $matches);
					$matches[1] = 'http://'.ENGINE_CURR.trim($matches[1]);
					$matches[3] = 0;
					$matches = clean_info($matches, array(1,2,3), 'windows-1251');
					price_change_detect($matches[1], $matches[2], $matches[3], date("d.m.y-H:i:s"), $request->options[10004], $request->options[10006], $request->options[10018], ENGINE_CURR, ENGINE_TYPE);
					$itemsArray[$matches[1]] = array(
						$matches[2], 
						$matches[3],
						date("d.m.y-H:i:s"),
						$request->options[10004],
						$request->options[10018],
						$request->url
					);			
					AngryCurl::add_debug_msg($matches[1].' | '.$matches[2].' | 0');
				}
			} 
		} else {
			AngryCurl::add_debug_msg('Регион не совпадает');
	    $bad_urls[$request->url] = array(
	    		$info['http_code'],
	    		$request->options[10004]
	    	);			
		}

  }
}

